/*

Design patterns in JavaScript are proven solutions to common problems that developers face when building  software. They provide a framework for organizing and structuring code, making it easier to understand, modify, and extend the code.

The JavaScript design patterns can be categorized into three primary groups: Creational Patterns, Structural Patterns, and Behavioral Patterns.
JavaScript design patterns provide you with a structured, repeatable way to tackle commonly occurring problems in JavaScript development.

Creational Design Patterns 
    are a category of design patterns that focus on the process of object creation in software development. They provide solutions for creating objects in a flexible and efficient manner while abstracting the actual instantiation process. 

Structural design patterns 
    are those that help solve problems around managing the structure (or schema) of JavaScript objects. These problems could include creating a relationship between two unlike objects or abstracting some features of an object away forspecific users.

Behavioral design patterns 
    are those that help solve problems around how control (and responsibility) is passed between various objects. 
*/